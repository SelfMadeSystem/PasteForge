    /**
     * @param x x
     * @param y y
     * @param r radius
     * @param ir inner radius
     * @param sta start angle
     * @param spa stop angle
     * @param ext extend (outer)
     * @param add amount to add for each turning thingy
     * @param color colour
     */
    public static void drawCircle(double x, double y, double r, double ir, float sta, float spa, float ext, float add, Color color) {
        if (spa == 0 || sta == spa)
            return;
        Tessellator tessellator = Tessellator.getInstance();
        BufferBuilder worldrenderer = tessellator.getBuffer();

        GlStateManager.enableBlend();
        GlStateManager.disableTexture2D();
        GlStateManager.tryBlendFuncSeparate(770, 771, 1, 0);
        GlStateManager.color(color.getRed() / 255F, color.getGreen() / 255F, color.getBlue() / 255F, color.getAlpha() / 255F);

        worldrenderer.begin(GL_QUADS, DefaultVertexFormats.POSITION);

        worldrenderer.pos(MathUtil.cos(sta - ext) * r + x, MathUtil.sin(sta - ext) * r + y, 0.0D).endVertex();
        worldrenderer.pos(MathUtil.cos(sta) * ir + x, MathUtil.sin(sta) * ir + y, 0.0D).endVertex();
        worldrenderer.pos(MathUtil.cos(sta + add) * ir + x, MathUtil.sin(sta + add) * ir + y, 0.0D).endVertex();
        worldrenderer.pos(MathUtil.cos(sta + add) * r + x, MathUtil.sin(sta + add) * r + y, 0.0D).endVertex();
        for (float d = sta; d < spa; d += add) {
            worldrenderer.pos(MathUtil.cos(d) * r + x, MathUtil.sin(d) * r + y, 0.0D).endVertex();
            worldrenderer.pos(MathUtil.cos(d) * ir + x, MathUtil.sin(d) * ir + y, 0.0D).endVertex();
            worldrenderer.pos(MathUtil.cos(d + add) * ir + x, MathUtil.sin(d + add) * ir + y, 0.0D).endVertex();
            worldrenderer.pos(MathUtil.cos(d + add + ext) * r + x, MathUtil.sin(d + add + ext) * r + y, 0.0D).endVertex();
        }
        tessellator.draw();
        GlStateManager.enableTexture2D();
        GlStateManager.disableBlend();
    }

    public static void drawRoundRect(double x1, double y1, double x2, double y2, double round, Color main) {
        if (Math.abs(x1 - x2) * 2 <= round || Math.abs(y1 - y2) * 2 <= round)
            round = Math.min(Math.abs(y1 - y2) * 2, Math.abs(x1 - x2) * 2);
        drawRect(GL_QUADS, x1, y1 + round, x2, y2 - round, main.getRGB());
        drawRect(GL_QUADS, x1 + round, y1, x2 - round, y2, main.getRGB());
        drawCircle(x1 + round, y1 + round, round, 0, 180, 270, 0, 10, main);
        drawCircle(x2 - round, y1 + round, round, 0, 270, 360, 0, 10, main);
        drawCircle(x2 - round, y2 - round, round, 0, 0, 90, 0, 10, main);
        drawCircle(x1 + round, y2 - round, round, 0, 90, 180, 0, 10, main);
    }

